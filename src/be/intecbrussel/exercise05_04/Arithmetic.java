package be.intecbrussel.exercise05_04;

public class Arithmetic {

    public static void main(String[] args) {

        int value1 = 5;
        int value2 = 3;

        System.out.println(value1 = value2);
        System.out.println(value1 += value2);
        System.out.println(value1 -= value2);
        System.out.println(value1 *= value2);
        System.out.println(value1 /= value2);
        System.out.println(value1 %= value2);
        System.out.println(value1 &= value2);
        System.out.println(value1 |= value2);
        System.out.println(value1 ^= value2);
        System.out.println(value1 <<= value2);
        System.out.println(value1 >>= value2);
        System.out.println(value1 >>>= value2);
    }
}
