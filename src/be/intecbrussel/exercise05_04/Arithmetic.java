package be.intecbrussel.exercise05_04;

public class Arithmetic {

    public static void main(String[] args) {

        boolean value1 = 3 < 5;
        boolean value2 = 5 < 3;

        System.out.println(value1);
        System.out.println(value2);
        System.out.println(value1 && value2);
        System.out.println(value1 || value2);
        System.out.println(!value1);
        System.out.println(!value2);
    }
}
