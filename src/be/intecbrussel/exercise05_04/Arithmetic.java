package be.intecbrussel.exercise05_04;

public class Arithmetic {

    public static void main(String[] args) {

        int number1 = 5;
        int number2 = 3;

        System.out.println(number1 < number2);
        System.out.println(number1 <= number2);
        System.out.println(number1 > number2);
        System.out.println(number1 >= number2);
        System.out.println(number1 == number2);
        System.out.println(number1 != number2);
    }
}
