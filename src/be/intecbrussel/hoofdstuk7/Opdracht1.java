package be.intecbrussel.hoofdstuk7;

public class Opdracht1 {

    public static void main(String[] args) {

        int[] numbers = {7, 14, 21, 28};

        for (int i: numbers) {

            System.out.println(i);
        }

        for (int i = 3; i >= 0 ; i--) {

            System.out.println(numbers[i]);
        }
    }
}
