package be.intecbrussel.exercise05_01;

public class DataTypesApp {

    public static void main(String[] args) {

        int a = 5;
        int b = ++a;
        int c = 7;
        int d = c++;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
